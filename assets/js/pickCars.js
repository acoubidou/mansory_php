    $(".boutonAll").on("click", function(){
        var AnneeGauche = $('#comparGaucheAnnee').text();
        var AnneeDroite = $('#comparDroiteAnnee').text();
        if (AnneeGauche && AnneeDroite != "-"){
            if (AnneeDroite > AnneeGauche) {
                $("#comparDroiteAnneeCase").css("border", "3px solid green");
                $("#comparGaucheAnneeCase").css("border", "1px solid black");
            } else if (AnneeDroite < AnneeGauche) {
                $("#comparGaucheAnneeCase").css("border", "3px solid green");
                $("#comparDroiteAnneeCase").css("border", "1px solid black");
            } else {
                $("#comparDroiteAnneeCase").css("border", "3px solid orange");
                $("#comparGaucheAnneeCase").css("border", "3px solid orange");
            }
        }
        var MoteurGauche = $('#comparGaucheMoteur').text();
        var MoteurDroite = $('#comparDroiteMoteur').text();
        if (MoteurGauche && MoteurDroite != "-"){
            MoteurDroite = MoteurDroite.substr(1);
            MoteurGauche = MoteurGauche.substr(1);
            if (Number(MoteurDroite) > Number(MoteurGauche)) {  
                $("#comparDroiteMoteurCase").css("border", "3px solid green");
                $("#comparGaucheMoteurCase").css("border", "1px solid black");
            } else if (Number(MoteurDroite) < Number(MoteurGauche)) {
                $("#comparGaucheMoteurCase").css("border", "3px solid green");
                $("#comparDroiteMoteurCase").css("border", "1px solid black");
            } else {
                $("#comparDroiteMoteurCase").css("border", "3px solid orange");
                $("#comparGaucheMoteurCase").css("border", "3px solid orange");
            }
        }
        var PuissanceGauche = $('#comparGauchePuissance').text();
        var PuissanceDroite = $('#comparDroitePuissance').text();
        if (PuissanceGauche && PuissanceDroite != "-"){
            PuissanceDroite = PuissanceDroite.slice(0, -2);
            PuissanceGauche = PuissanceGauche.slice(0, -2);
            if (Number(PuissanceDroite) > Number(PuissanceGauche)) {
                $("#comparDroitePuissanceCase").css("border", "3px solid green");
                $("#comparGauchePuissanceCase").css("border", "1px solid black");
            } else if (Number(PuissanceDroite) < Number(PuissanceGauche)) {
                $("#comparGauchePuissanceCase").css("border", "3px solid green");
                $("#comparDroitePuissanceCase").css("border", "1px solid black");
            } else {
                $("#comparDroitePuissanceCase").css("border", "3px solid orange");
                $("#comparGauchePuissanceCase").css("border", "3px solid orange");
            }
        }
        var CoupleGauche = $('#comparGaucheCouple').text();
        var CoupleDroite = $('#comparDroiteCouple').text();
        if (CoupleGauche && CoupleDroite != "-"){
            CoupleDroite = CoupleDroite.slice(0, -2);
            CoupleGauche = CoupleGauche.slice(0, -2);
            if (Number(CoupleDroite) > Number(CoupleGauche)) {
                $("#comparDroiteCoupleCase").css("border", "3px solid green");
                $("#comparGaucheCoupleCase").css("border", "1px solid black");
            } else if (Number(CoupleDroite) < Number(CoupleGauche)) {
                $("#comparGaucheCoupleCase").css("border", "3px solid green");
                $("#comparDroiteCoupleCase").css("border", "1px solid black");
            } else {
                $("#comparDroiteCoupleCase").css("border", "3px solid orange");
                $("#comparGaucheCoupleCase").css("border", "3px solid orange");
            }
        }
        var AccelerationGauche = $('#comparGaucheAcceleration').text();
        var AccelerationDroite = $('#comparDroiteAcceleration').text();
        if (AccelerationGauche && AccelerationDroite != "-"){
            AccelerationDroite = AccelerationDroite.slice(0, -1);
            AccelerationGauche = AccelerationGauche.slice(0, -1);
            if (Number(AccelerationDroite) < Number(AccelerationGauche)) {
                $("#comparDroiteAccelerationCase").css("border", "3px solid green");
                $("#comparGaucheAccelerationCase").css("border", "1px solid black");
            } else if (Number(AccelerationDroite) > Number(AccelerationGauche)) {
                $("#comparGaucheAccelerationCase").css("border", "3px solid green");
                $("#comparDroiteAccelerationCase").css("border", "1px solid black");
            } else {
                $("#comparDroiteAccelerationCase").css("border", "3px solid orange");
                $("#comparGaucheAccelerationCase").css("border", "3px solid orange");
            }
        }
        var VitesseGauche = $('#comparGaucheVitesse').text();
        var VitesseDroite = $('#comparDroiteVitesse').text();
        if (VitesseGauche && VitesseDroite != "-"){
            VitesseDroite = VitesseDroite.slice(0, -4);
            VitesseGauche = VitesseGauche.slice(0, -4);
            if (Number(VitesseDroite) > Number(VitesseGauche)) {
                $("#comparDroiteVitesseCase").css("border", "3px solid green");
                $("#comparGaucheVitesseCase").css("border", "1px solid black");
            } else if (Number(VitesseDroite) < Number(VitesseGauche)) {
                $("#comparGaucheVitesseCase").css("border", "3px solid green");
                $("#comparDroiteVitesseCase").css("border", "1px solid black");
            } else {
                $("#comparDroiteVitesseCase").css("border", "3px solid orange");
                $("#comparGaucheVitesseCase").css("border", "3px solid orange");
            }
        }
        var PrixGauche = $('#comparGauchePrix').text();
        var PrixDroite = $('#comparDroitePrix').text();
        if (PrixGauche && PrixDroite != "-"){
            PrixDroite = PrixDroite.replace(/\s+/g, '');
            PrixDroite = PrixDroite.slice(0, -1);
            PrixGauche = PrixGauche.replace(/\s+/g, '');
            PrixGauche = PrixGauche.slice(0, -1);
            if (Number(PrixDroite) < Number(PrixGauche)) {
                $("#comparDroitePrixCase").css("border", "3px solid green");
                $("#comparGauchePrixCase").css("border", "1px solid black");
            } else if (Number(PrixDroite) > Number(PrixGauche)) {
                $("#comparGauchePrixCase").css("border", "3px solid green");
                $("#comparDroitePrixCase").css("border", "1px solid black");
            } else {
                $("#comparDroitePrixCase").css("border", "3px solid orange");
                $("#comparGauchePrixCase").css("border", "3px solid orange");
            }
        }
    });
